/**
 * Group templates by category
 */
export function getTemplatesByCategory() {
  const categories = {};

  contentTypeTemplates.forEach((template) => {
    if (!categories[template.category]) {
      categories[template.category] = [];
    }
    categories[template.category].push(template);
  });

  return categories;
}

/**
 * Get a specific template by ID
 */
export function getTemplateById(templateId) {
  return contentTypeTemplates.find((template) => template.id === templateId);
}

/**
 * Get templates by category name
 */
export function getTemplatesByCategoryName(categoryName) {
  return contentTypeTemplates.filter((template) => template.category === categoryName);
}
